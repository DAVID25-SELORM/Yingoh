import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const migrationsDir = path.join(root, 'supabase', 'migrations');
const outputDir = path.join(root, 'supabase', 'final');
const outputFile = path.join(outputDir, 'yingoh_question_bank_complete.sql');
const firstMigration = '20260702160000';
const lastMigration = '20260702310000';

const files = fs.readdirSync(migrationsDir)
  .filter((name) => name.endsWith('.sql'))
  .filter((name) => {
    const timestamp = name.slice(0, 14);
    return timestamp >= firstMigration && timestamp <= lastMigration;
  })
  .sort();

if (!files.some((name) => name.startsWith(firstMigration))) {
  throw new Error(`Missing first package migration ${firstMigration}.`);
}
if (!files.some((name) => name.startsWith(lastMigration))) {
  throw new Error(`Missing final package migration ${lastMigration}.`);
}

const sections = files.map((name) => {
  const sql = fs.readFileSync(path.join(migrationsDir, name), 'utf8').trim();
  return `\n-- ============================================================================\n-- ${name}\n-- ============================================================================\n${sql}\n`;
});

const header = `-- YINGOH COMPLETE QUESTION-BANK PACKAGE
-- Generated by: npm run package:questions
-- Migration count: ${files.length}
-- Apply this entire file once in the Supabase SQL Editor.
-- The transaction rolls back automatically if any final guardrail fails.

begin;
`;
const footer = `
commit;

-- Expected result: "Success. No rows returned"
`;

fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(outputFile, header + sections.join('') + footer, 'utf8');
console.log(`Built ${path.relative(root, outputFile)} from ${files.length} migrations.`);
